package org.add;

public class Calculator
{

    public  int adds(int a, int b)
    {
        return a+b;
    }

    public double sub(double no1, double no2)
    {
        return no1-no2;
    }

    public int mul(int m, int n)
    {
        return  m * n;
    }

    public double div(double num1, double num2)
    {
        return num1/num2;
    }
}
